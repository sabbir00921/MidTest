import { Bounce, toast } from "react-toastify";

export const success = (prop) => {
  toast.success(prop || "success", {
    position: "top-right",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: false,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
    transition: Bounce,
  });
};

